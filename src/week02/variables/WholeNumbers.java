package week02.variables;

public class WholeNumbers {
    public static void main(String[] args) {
        //declare variable students
        int students;

        //assing 128 to students variable.intialize students with 128
        students = 128;

        System.out.println("students");
        System.out.println(students);

        //change value in students variable to 150

        students = 150;
        System.out.println(students);
        //declare + initialize int variable teachers
        int teachers = 5;
        System.out.println("teachers");
        System.out.println(teachers);

        teachers = 15;
        System.out.println("teachers");

        //byte ,short , int ,long
        byte n1 = 111;
        short n2 = 23000;
        int n3 = 23423423;
        long n4 = 6545664654l;

        System.out.println(n1);
        System.out.println(n2);
        System.out.println(n3);
        System.out.println(n4);

        byte screenSize =16;
        screenSize =13;
        System.out.println(screenSize);



    }

}
