package week08;

public class extractAddress {































































































































































































































    /*
    "address=123 Main st, New York, NY" -> "123 Main st, New York, NY"
     */
    public static void main(String[] args) {
        String address = "address=123 Main st, New York, NY";

        System.out.println(address.substring(8));
        String addr = address.substring(8);
        System.out.println("addr = " + addr);

        System.out.println(address.substring(0,7));

        String str = "city=Chicago";
        String city = str.substring(5);
        System.out.println("city = " + city);

        System.out.println(str.indexOf("="));

        System.out.println(str.substring(str.indexOf("=")+1));

        String results = "1747546244 results";
        String count = results.substring(0, results.indexOf(" "));
        System.out.println("count = " + count);
    }
}